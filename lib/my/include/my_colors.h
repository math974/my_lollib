#define BLACK               "\033[30m"
#define BOLD_BLACK          "\033[30;1m"
#define BACKGROUD_BLACK     "\033[30;3m"
#define UNDERSCORE_BLACK    "\033[30;4m"
#define RED                 "\033[31m"
#define BOLD_RED            "\033[31;1m"
#define BACKGROUD_RED       "\033[31;3m"
#define UNDERSCORE_RED      "\033[31;4m"
#define GREEN               "\033[32m"
#define BOLD_GREEN          "\033[32;1m"
#define BACKGROUD_GREEN     "\033[32;3m"
#define UNDERSCORE_GREEN    "\033[32;4m"
#define YELLOW              "\033[33m"
#define BOLD_YELLOW         "\033[33;1m"
#define BACKGROUD_YELLOW    "\033[33;3m"
#define UNDERSCORE_YELLOW   "\033[33;4m"
#define BLUE                "\033[34m"
#define BOLD_BLUE           "\033[34;1m"
#define BACKGROUD_BLUE      "\033[34;3m"
#define UNDERSCORE_BLUE     "\033[34;4m"
#define PURPLE              "\033[35m"
#define BOLD_PURPLE         "\033[35;1m"
#define BACKGROUD_PURPLE    "\033[35;3m"
#define UNDERSCORE_PURPLE   "\033[35;4m"
#define CYAN                "\033[36m"
#define BOLD_CYAN           "\033[36;1m"
#define BACKGROUD_CYAN      "\033[36;3m"
#define UNDERSCORE_CYAN     "\033[36;4m"
#define WHITE               "\033[37m"
#define BOLD_WHITE          "\033[37;1m"
#define BACKGROUD_WHITE     "\033[37;3m"
#define UNDERSCORE_WHITE    "\033[37;4m"
#define RESET               "\033[0m"

#endif /* !MY_COLORS_H_ */